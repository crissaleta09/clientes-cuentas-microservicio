
package bankaccount.out.entity;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.JoinColumn;
import jakarta.persistence.ManyToOne;
import jakarta.persistence.Table;

@Entity
@Table(name = "cuenta_bancaria")
public class CuentaBancariaEntity {
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name ="id")
    private Long id; // Primary key for bank account entity autogenerated
	@Column(name ="tipo_cuenta")
    private String tipo_cuenta;
    @Column(name ="total")
    private Double total;

    @ManyToOne
    @JoinColumn(name="dni_cliente") // Foreign key to ClienteEntity
    private ClienteEntity cliente; // Many bank accounts can belong to one customer
    
    public CuentaBancariaEntity() {}

    public CuentaBancariaEntity(Long id, String dni_cliente, String tipo_cuenta, Double total, ClienteEntity cliente) {
        this.id = id;
        this.tipo_cuenta = tipo_cuenta;
        this.total = total;
        this.cliente = cliente;
    }

    public Long getId() { return id; }
    public void setId(Long id) { this.id = id; }
    public String getTipoCuenta() { return tipo_cuenta; }
    public void setTipoCuenta(String tipo_cuenta) { this.tipo_cuenta = tipo_cuenta; }
    public Double getTotal() { return total; }
    public void setTotal(Double total) { this.total = total; }
    public ClienteEntity getCliente() { return cliente; }
    public void setCliente(ClienteEntity cliente) { this.cliente = cliente; }
}
